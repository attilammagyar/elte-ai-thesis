import numpy as np
from scipy import signal

def distort(samples, gain_db=12.0, sample_rate=44100.0, channels=2):
    """
    Applies gain increase and tanh non-linearity to an audio signal
    with anti-aliasing using oversampling.

    Parameters:
    samples (np.ndarray): Input audio signal of shape (N, channels).
                          The samples are float32 numbers.
    gain_db (float): Gain to apply in decibels. Default is 12.0 dB.
    sample_rate (float): Sampling rate of the audio signal in Hz.
                         Default is 44100.0 Hz.
    channels (int): Number of audio channels in the input signal.
                    Default is 2.

    Returns:
    np.ndarray: Distorted audio signal, with the same shape and dtype as
                the input `samples` array.
    """

    # Oversampling factor. A factor of 4 is chosen as a good balance
    # between anti-aliasing effectiveness and computational cost.
    # Higher factors (e.g., 8x, 16x) provide better aliasing suppression
    # at the cost of increased computation.
    OS_factor = 4

    # Store the original data type of the input samples.
    # The problem specifies float32, but this makes the function more robust.
    original_dtype = samples.dtype

    # Handle the edge case of an empty input array.
    # np.zeros_like ensures the output has the correct shape and dtype.
    if samples.shape[0] == 0:
        return np.zeros_like(samples)

    # 1. Convert gain from decibels (dB) to a linear scale.
    gain_linear = 10**(gain_db / 20.0)

    # 2. Upsample the audio signal.
    # This effectively increases the Nyquist frequency, providing "headroom"
    # for harmonics that will be generated by the non-linear tanh function.
    # `signal.resample_poly` is efficient for rational resampling factors (integer up/down).
    # It processes along `axis=0` (the time axis). Given `samples` is (N, channels),
    # this correctly resamples each channel independently.
    upsampled_samples = signal.resample_poly(samples, OS_factor, 1, axis=0)

    # 3. Apply the linear gain to the upsampled signal.
    gained_upsampled_samples = upsampled_samples * gain_linear

    # 4. Apply the tanh non-linear waveshaping function.
    # `np.tanh` provides a soft-clipping effect. It's a common choice for
    # distortion and saturation in audio processing.
    distorted_upsampled_samples = np.tanh(gained_upsampled_samples)

    # 5. Downsample the signal back to the original sample rate.
    # `signal.resample_poly` applies an anti-aliasing (low-pass) filter
    # before decimating the signal. This filter removes harmonics above the
    # original Nyquist frequency that were introduced by the tanh non-linearity,
    # thereby minimizing aliasing artifacts in the output signal.
    distorted_signal = signal.resample_poly(distorted_upsampled_samples, 1, OS_factor, axis=0)

    # Ensure the output data type matches the original input data type.
    # While recent versions of SciPy (>=1.8) might preserve float32 for float32 inputs,
    # an explicit cast guarantees consistency, especially if the internal processing
    # of `resample_poly` uses float64, or for older SciPy versions.
    if distorted_signal.dtype != original_dtype:
        distorted_signal = distorted_signal.astype(original_dtype)
    
    # The output length from `resample_poly` when upsampling by OS_factor and then
    # downsampling by OS_factor should result in the original length N:
    # N_upsampled = ceil(N_input * OS_factor / 1) = N_input * OS_factor
    # N_output = ceil(N_upsampled * 1 / OS_factor)
    #          = ceil((N_input * OS_factor) * 1 / OS_factor) = N_input.
    # Thus, explicit length adjustment is typically not required.

    # The parameters `sample_rate` and `channels` from the function signature
    # are not directly used in this implementation. `sample_rate` is not needed
    # because the oversampling approach and `resample_poly` define filter
    # characteristics relative to the (current) Nyquist frequency, not absolute
    # frequencies. `channels` is implicitly handled by `samples.shape` and the
    # `axis=0` argument in `resample_poly`.

    return distorted_signal


