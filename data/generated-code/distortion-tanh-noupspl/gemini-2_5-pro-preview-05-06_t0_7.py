import numpy as np
from scipy import signal

def distort(samples, gain_db=12.0, sample_rate=44100.0, channels=2):
    """
    Applies gain increase and tanh non-linearity to an audio signal
    while keeping aliasing to a minimum without oversampling.
    """

    # Parameter `channels` is part of the signature but not explicitly used if samples.shape is trusted.
    # samples are expected to be of shape (N, channels).

    # Handle empty input signal (no samples)
    if samples.shape[0] == 0:
        return np.zeros_like(samples) 

    # 1. Convert gain_db to linear gain factor
    gain_linear = 10**(gain_db / 20.0)

    # 2. Apply gain to the input samples
    boosted_samples = samples * gain_linear

    # 3. Apply tanh non-linearity (soft clipping)
    distorted_tanh = np.tanh(boosted_samples)

    # 4. Antialiasing: Apply a low-pass filter after non-linearity
    # This attenuates high-frequency harmonics generated by the distortion
    # that could cause aliasing.

    # Filter design parameters
    filter_order = 2  # Order of the Butterworth filter
    
    # Normalized cutoff frequency (Wn), as a fraction of the Nyquist frequency.
    # e.g., 0.9 means cutoff at 90% of Nyquist.
    # (Nyquist frequency = sample_rate / 2.0)
    normalized_cutoff = 0.9 

    # Design the Butterworth low-pass filter using second-order sections (SOS)
    # SOS format is generally preferred for numerical stability over b,a format.
    sos_coefficients = signal.butter(filter_order, 
                                     normalized_cutoff, 
                                     btype='low', 
                                     analog=False, 
                                     output='sos')

    # Apply the filter.
    # signal.sosfiltfilt applies the filter forward and then backward, 
    # resulting in zero phase distortion and an effective filter order of 2 * filter_order.
    # It operates along axis=0 (the time dimension for each channel).
    
    # sosfiltfilt with padtype='constant' (zero-padding) requires the input signal 
    # length along the filtered axis (samples.shape[0]) to be greater than 
    # the default padlen for SOS, which is (section_length - 1) = (3 - 1) = 2.
    # Thus, samples.shape[0] must be >= 3.
    if samples.shape[0] >= 3:
        distorted_signal = signal.sosfiltfilt(sos_coefficients, 
                                              distorted_tanh, 
                                              axis=0,
                                              padtype='constant') # Handles shorter signals robustly
    else:
        # For very short signals (N < 3 samples), filtering might not be meaningful
        # or sosfiltfilt may not be applicable with default padding.
        # Return the unfiltered distorted signal in this edge case.
        # A copy is made to ensure the output is a new array, not a view.
        distorted_signal = distorted_tanh.copy()
    
    return distorted_signal



