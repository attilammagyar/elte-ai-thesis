import numpy as np
from scipy import signal

def distort(samples, gain_db=12.0, sample_rate=44100.0, channels=2):
    """
    Applies gain increase and tanh non-linearity to an audio signal
    with anti-aliasing using a low-pass filter.

    Parameters:
    samples (np.ndarray): Input audio signal, shape (N, channels), float32.
    gain_db (float): Gain to apply in decibels.
    sample_rate (float): Sampling rate of the audio signal in Hz.
    channels (int): Number of audio channels. (Note: The implementation primarily
                    derives channel count from samples.shape[1] via axis=0 processing).

    Returns:
    np.ndarray: Distorted audio signal, same shape and dtype as samples.
    """

    # 1. Convert gain from dB to linear scale
    # Formula: linear_gain = 10^(gain_db / 20)
    linear_gain = 10.0**(gain_db / 20.0)

    # 2. Apply gain to the input samples
    # This operation is vectorized for efficiency.
    gained_samples = samples * linear_gain

    # 3. Apply tanh non-linearity
    # np.tanh is a hyperbolic tangent function. It acts as a soft-clipper,
    # mapping its input to the range [-1, 1].
    # This non-linear process is what generates new harmonic content.
    distorted_no_aa = np.tanh(gained_samples)

    # 4. Apply anti-aliasing filter (Butterworth low-pass filter)
    # The purpose of this filter is to attenuate high-frequency harmonics
    # generated by the tanh function that lie above the Nyquist frequency
    # (sample_rate / 2). This helps to minimize audible aliasing artifacts.

    # Filter design parameters:
    filter_order = 4  # A 4th order filter provides a good balance between
                      # effectiveness (rolloff steepness) and computational cost.
    
    # Nyquist frequency is half the sampling rate.
    nyquist_freq = sample_rate / 2.0
    
    # Normalized cutoff frequency for the low-pass filter.
    # This is expressed as a fraction of the Nyquist frequency.
    # A value of 0.9 means the cutoff is at 90% of the Nyquist frequency.
    # E.g., for 44.1kHz sample rate, Nyquist is 22050 Hz, cutoff is ~19845 Hz.
    # This aims to preserve most of the audible spectrum while reducing aliasing.
    # The value must be in the interval (0, 1) for scipy.signal.butter.
    normalized_cutoff = 0.9 

    # Design the Butterworth low-pass filter.
    # 'sos' (Second-Order Sections) output format is chosen because it is
    # generally more numerically stable for IIR filters than the 'ba'
    # (numerator/denominator coefficients) format.
    sos_coefficients = signal.butter(filter_order, normalized_cutoff, 
                                     btype='low', analog=False, output='sos')

    # Apply the filter using sosfiltfilt.
    # sosfiltfilt performs zero-phase filtering by processing the signal
    # in both forward and reverse directions. This avoids phase distortion,
    # which is often desirable in audio processing.
    # The filter is applied along axis=0, meaning it processes each channel
    # independently, assuming 'samples' is of shape (N, channels).

    # sosfiltfilt requires the signal length along the filtered axis to be
    # greater than 'padlen'. For biquad sections (as in SOS), padlen is
    # typically 3 * (number_of_coefficients_per_section - 1).
    # Each SOS section has 6 coefficients, so padlen = 3 * (6 - 1) = 15.
    # Thus, the number of samples N must be > 15.
    if samples.shape[0] > 15: 
        distorted_samples = signal.sosfiltfilt(sos_coefficients, distorted_no_aa, axis=0)
    else:
        # If the signal is too short, filtering might fail or produce unreliable results.
        # In such a scenario, we fall back to returning the signal without anti-aliasing.
        # This means aliasing will not be minimized for these very short segments.
        # This is a pragmatic choice for handling this edge case.
        distorted_samples = distorted_no_aa
        
    # Ensure the output array has the same data type as the input 'samples' array
    # (e.g., float32). SciPy filtering functions sometimes default to float64.
    return distorted_samples.astype(samples.dtype)


