def is_separator(token):
    # A separator is a token with no alphanumeric character.
    return not any(c.isalnum() for c in token)

def tokenize_phrase(phrase):
    # Given a phrase like "AAA, BBB" or "AAA BBB", split into tokens (words & separators)
    # We treat runs of alphanumerics as words, everything else as separators.
    tokens = []
    i = 0
    while i < len(phrase):
        if phrase[i].isalnum():
            start = i
            while i < len(phrase) and phrase[i].isalnum():
                i += 1
            tokens.append(phrase[start:i])
        else:
            start = i
            while i < len(phrase) and not phrase[i].isalnum():
                i += 1
            tokens.append(phrase[start:i])
    return tokens

class TrieNode:
    __slots__ = ['children', 'entries', 'is_word', 'is_separator']

    def __init__(self, is_word=None, is_separator=None):
        self.children = dict()
        self.entries = set()  # set of entry ids for phrases ending here
        self.is_word = is_word
        self.is_separator = is_separator

def build_dictionary_index(dictionary: collections.abc.Mapping) -> object:
    """
    Build an index from a dictionary for fast lookup of words and compound phrases.
    """
    trie_root = TrieNode()
    word_dict = dict()  # lowercased word -> set of entry ids

    for phrase, entry in dictionary.items():
        tokens = tokenize_phrase(phrase)
        # Determine for each token if it's a word or separator
        types = [not is_separator(tok) for tok in tokens]
        node = trie_root
        for i, tok in enumerate(tokens):
            key = tok.lower()
            is_word_token = types[i]
            if key not in node.children:
                node.children[key] = TrieNode(
                    is_word=is_word_token,
                    is_separator=not is_word_token
                )
            node = node.children[key]
        node.entries.add(entry)
        # Also record single words for fast lookup
        if len(tokens) == 1 and types[0]:
            key = tokens[0].lower()
            word_dict.setdefault(key, set()).add(entry)

    return {
        'trie_root': trie_root,
        'word_dict': word_dict
    }

def annotate(tokens: collections.abc.Iterable[str], dictionary_index: object) -> collections.abc.Iterable[tuple[str, collections.abc.Set]]:
    """
    Annotate tokens with entries from the dictionary.
    """
    tokens = list(tokens)
    n = len(tokens)
    # Prepare annotation sets per token
    annotations = [set() for _ in tokens]
