    word_dict = dictionary_index['word_dict']

    # Precompute token types for fast access
    token_types = [not is_separator(tok) for tok in tokens]

    # Match compound phrases using the trie
    for i in range(n):
        # Only start phrase matching at word tokens
        if not token_types[i]:
            continue
        node = trie_root
        idxs = []
        found_any = False
        j = i
        # To match phrases, we must not start or end on a separator.
        # So we start at a word, and track the span.
        while j < n:
            key = tokens[j].lower()
            is_word = token_types[j]
            if key in node.children:
                node = node.children[key]
                idxs.append(j)
                # If this node ends a phrase, annotate the span
                if node.entries:
                    # Only annotate if span starts and ends on word tokens
                    if token_types[i] and token_types[j]:
                        entry_ids = node.entries
                        # Annotate all tokens in span
                        for k in range(i, j + 1):
                            # For separators: only annotate inner separators
                            if not token_types[k]:
                                if k == i or k == j:
                                    continue
                            annotations[k].update(entry_ids)
                j += 1
            else:
                # If current token is a separator and node has *any* separator child, try all
                if not is_word:
                    matched = False
                    for child_key, child_node in node.children.items():
                        if child_node.is_separator and child_key == key:
                            node2 = child_node
                            idxs2 = idxs + [j]
                            # Continue matching from here
                            node = node2
                            idxs = idxs2
                            matched = True
                            break
                    if matched:
                        j += 1
                        continue
                break

    # Annotate individual words
    for i, tok in enumerate(tokens):
        if token_types[i]:
            entry_ids = word_dict.get(tok.lower(), set())
            annotations[i].update(entry_ids)

    # Prepare output
    annotated_tokens = []
    for tok, ann in zip(tokens, annotations):
        annotated_tokens.append((tok, set(ann)))
    return annotated_tokens


