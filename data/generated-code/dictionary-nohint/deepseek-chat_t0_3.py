import collections.abc

def build_dictionary_index(dictionary: collections.abc.Mapping) -> object:
    # Build a case-insensitive dictionary and a trie for phrase lookup
    index = {
        'words': set(),  # Individual words
        'phrases': {},    # Trie for phrases
    }
    
    for key, value in dictionary.items():
        lower_key = key.lower()
        parts = lower_key.split()
        
        if len(parts) == 1:
            index['words'].add((parts[0], value))
        else:
            current = index['phrases']
            for part in parts[:-1]:
                if part not in current:
                    current[part] = {}
                current = current[part]
            last_part = parts[-1]
            if last_part not in current:
                current[last_part] = []
            current[last_part].append((len(parts), value))
    
    return index

