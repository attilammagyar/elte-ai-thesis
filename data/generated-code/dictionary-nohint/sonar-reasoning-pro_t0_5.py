import re
import collections.abc
from collections import defaultdict

def build_dictionary_index(dictionary: collections.abc.Mapping) -> object:
    individual_entries = defaultdict(set)
    phrase_entries = defaultdict(set)
    
    for key, entry in dictionary.items():
        # Split the key into words using non-alphanumeric characters as delimiters
        words = re.findall(r'\w+', key.lower())
        if words:
            # Add to phrase entries
            phrase_key = tuple(words)
            phrase_entries[phrase_key].add(entry)
            # Add to individual entries if the phrase is a single word
            if len(words) == 1:
                individual_entries[words[0]].add(entry)
    
    return (individual_entries, phrase_entries)

def annotate(tokens: collections.abc.Iterable[str], dictionary_index: object) -> collections.abc.Iterable[tuple[str, set]]:
    individual_entries, phrase_entries = dictionary_index
    annotated_tokens = [(token, set()) for token in tokens]
    
    # Collect words and their positions
    words_and_positions = []
    for pos, token in enumerate(tokens):
        if re.fullmatch(r'\w+', token):
            words_and_positions.append((pos, token.lower()))
    
    # Check all possible phrases
    for start in range(len(words_and_positions)):
        for end in range(start, len(words_and_positions)):
            phrase = tuple(wp[1] for wp in words_and_positions[start:end+1])
            if phrase in phrase_entries:
                entries = phrase_entries[phrase]
                first_pos = words_and_positions[start][0]
                last_pos = words_and_positions[end][0]
                for i in range(first_pos, last_pos + 1):
                    annotated_tokens[i][1].update(entries)
    
    # Check individual words
    for pos, token in enumerate(tokens):
        if re.fullmatch(r'\w+', token):
            lowercase_token = token.lower()
            if lowercase_token in individual_entries:
                annotated_tokens[pos][1].update(individual_entries[lowercase_token])
    
    # Convert sets to immutable if needed, but tests expect sets
    return [(token, annotations) for token, annotations in annotated_tokens]


