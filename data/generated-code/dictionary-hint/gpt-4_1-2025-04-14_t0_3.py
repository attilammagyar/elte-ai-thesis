# Define what is a separator
_SEPARATOR_CHARS = set(string.whitespace + string.punctuation)
_SEPARATOR_TOKEN = '__SEP__'

def is_separator(token):
    # A separator is a token that is entirely whitespace or punctuation
    return all(c in _SEPARATOR_CHARS for c in token)

def tokenize_phrase(phrase):
    # Tokenize a dictionary phrase into words and separators
    # e.g. "AAA, BBB" -> ['AAA', ',', ' ', 'BBB']
    tokens = []
    i = 0
    while i < len(phrase):
        if phrase[i] in _SEPARATOR_CHARS:
            # Collect contiguous separators as one token
            start = i
            while i < len(phrase) and phrase[i] in _SEPARATOR_CHARS:
                i += 1
            tokens.append(phrase[start:i])
        else:
            # Collect contiguous non-separators as one token
            start = i
            while i < len(phrase) and phrase[i] not in _SEPARATOR_CHARS:
                i += 1
            tokens.append(phrase[start:i])
    return tokens

def normalize_token(token):
    # Lowercase and normalize separators
    if is_separator(token):
        return _SEPARATOR_TOKEN
    else:
        return token.lower()

class TrieNode:
    __slots__ = ['children', 'entries']
    def __init__(self):
        self.children = dict()
        self.entries = set()

def build_dictionary_index(dictionary: collections.abc.Mapping) -> object:
    root = TrieNode()
    for phrase, entry in dictionary.items():
        tokens = tokenize_phrase(phrase)
        norm_tokens = [normalize_token(tok) for tok in tokens]
        node = root
        for tok in norm_tokens:
            if tok not in node.children:
                node.children[tok] = TrieNode()
            node = node.children[tok]
        node.entries.add(entry)
    return root

