    # 2x to 8x are common; 4x is a reasonable default.
    oversample_factor = 4 

    # 1. Convert gain_db to linear gain
    # The formula for converting dB to linear scale is: linear = 10^(dB / 20)
    linear_gain = 10**(gain_db / 20.0)

    # 2. Apply initial gain to the samples
    # This must be done before applying the non-linear distortion.
    samples_gained = samples * linear_gain
    
